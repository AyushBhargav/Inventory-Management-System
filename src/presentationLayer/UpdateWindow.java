    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jComboBox2.getSelectedItem() != null && jComboBox3.getSelectedItem() != null && !jTextField2.getText().equals("")) {
            String oldItemName = jComboBox3.getSelectedItem().toString();
            String newItemName = jTextField2.getText();
            ItemService is = new ItemServiceImplementation();
            int id = is.getId(oldItemName);
            double price = is.getPrice(oldItemName);
            int freq = is.getSells(oldItemName);
            int stock = is.getSells(oldItemName);
            Item item = new Item(id,newItemName,price,freq,stock);
            is.updateToDb(item);
            this.dispose();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

