    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jComboBox1.getSelectedItem() != null && !jTextField1.getText().equals("")) {
            String oldCatName = jComboBox1.getSelectedItem().toString();
            String newCatName = jTextField1.getText();
            CategoryService cs = new CategoryServiceImplementation();
            int id = cs.getId(oldCatName);
            int stock = cs.getStock(id);
            int freq = cs.getFrequency(id);
            Category cat = new Category(id,newCatName,stock,freq);
            cs.updateToDb(cat);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

